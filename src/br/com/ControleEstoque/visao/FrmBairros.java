    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed
	if (txt_nomeBairro.getText().isEmpty()) {
	    JOptionPane.showMessageDialog(null, "H√° campos a serem preenchidos");
	} else {
	    mod.setNome_bairro(txt_nomeBairro.getText());
	    mod.setNome_cidade(cb_cidade.getSelectedItem().toString());
	    control.AdicionarBairro(mod);
	    preencherTabela("select * from bairro inner join cidade on id_cidadef = id_cidade order by nome_bairro");
	}
	txt_nomeBairro.setText("");
    }//GEN-LAST:event_bt_salvarActionPerformed

