     public void preencherCidades() {
	conex.Conexao();

	try {
	    conex.executaSql("select * from cidade order by nome_cidade");
	    conex.rs.first();
	    cb_cidade.removeAllItems();
	    do {
		cb_cidade.addItem(conex.rs.getString("nome_cidade"));
	    } while (conex.rs.next());
	} catch (SQLException ex) {
	    JOptionPane.showMessageDialog(null, "Erro ao preencher a combobox cidade" + ex);
	}
	conex.desconecta();
    }// FIM DO METODO

    public void preencherTabela(String sql) {
	ArrayList dados = new ArrayList();
	String[] colunas = new String[]{"Id", "Bairro", "Cidade"};
	conex.Conexao();
	conex.executaSql(sql);
	try {
	    conex.rs.first();
	    do {
		dados.add(new Object[]{conex.rs.getInt("id_bairro"), conex.rs.getString("nome_bairro"), conex.rs.getString("nome_cidade")});
	    } while (conex.rs.next());

	} catch (Exception ex) {
	    JOptionPane.showMessageDialog(null, "Erro ao preencher ArrayList" + ex);
	}
	ModeloTabela modelo = new ModeloTabela(dados, colunas);
	tbl_bairros.setModel(modelo);
	tbl_bairros.getColumnModel().getColumn(0).setPreferredWidth(70);
	tbl_bairros.getColumnModel().getColumn(0).setResizable(false);

	tbl_bairros.getColumnModel().getColumn(1).setPreferredWidth(300);
	tbl_bairros.getColumnModel().getColumn(1).setResizable(false);

	tbl_bairros.getColumnModel().getColumn(2).setPreferredWidth(165);
	tbl_bairros.getColumnModel().getColumn(2).setResizable(false);

	tbl_bairros.getTableHeader().setReorderingAllowed(false);
	tbl_bairros.setAutoResizeMode(tbl_bairros.AUTO_RESIZE_OFF);
	tbl_bairros.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
	conex.desconecta();
    }

