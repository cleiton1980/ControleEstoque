    private void bt_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pesquisarActionPerformed
	String pesquisa = txt_pesquisar.getText();
	if (pesquisa.equals("")) {
	    JOptionPane.showMessageDialog(null, "Digite um Bairro a ser pesquisado"); 
	} else {
	mod.setPesquisa(txt_pesquisar.getText());
	ModeloBairros model = control.pesquisarBairros(mod);
	lbl_idBairro.setText(String.valueOf(model.getId_bairro()));
	txt_nomeBairro.setText(model.getNome_bairro());
	cb_cidade.setSelectedItem(model.getNome_cidade());
	bt_editar.setEnabled(true);
	bt_excluir.setEnabled(true);
	}
    }//GEN-LAST:event_bt_pesquisarActionPerformed

    private void tbl_bairrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_bairrosMouseClicked
	String nome_bairro = "" + tbl_bairros.getValueAt(tbl_bairros.getSelectedRow(), 1);
	conex.Conexao();
	conex.executaSql("select * from bairro inner join cidade on id_cidadef = id_cidade where nome_bairro='" + nome_bairro + "'");
	try {
	    conex.rs.first();
	    lbl_idBairro.setText(Integer.toString(conex.rs.getInt("id_bairro")));
	    txt_nomeBairro.setText(conex.rs.getString("nome_bairro"));
	    cb_cidade.setSelectedItem(conex.rs.getString("nome_cidade"));
	} catch (Exception ex) {
	    JOptionPane.showMessageDialog(null, "Erro ao setar campos " + ex);
	}
	conex.desconecta();
	bt_editar.setEnabled(true);
	bt_excluir.setEnabled(true);
	bt_salvar.setEnabled(false);
	txt_nomeBairro.setEnabled(true);
	cb_cidade.setEnabled(true);
    }//GEN-LAST:event_tbl_bairrosMouseClicked

