    public void preencherTabela(String sql) {
	ArrayList dados = new ArrayList();
	String[] colunas = new String[]{"Id", "Nome", "Login", "Perfil", "Senha"};
	conex.Conexao();
	conex.executaSql(sql);
	try {
	    conex.rs.first();
	    do {
		dados.add(new Object[]{conex.rs.getInt("id_usuario"), conex.rs.getString("nome_usuario"), conex.rs.getString("login"), conex.rs.getString("perfil"), conex.rs.getString("senha")});
	    } while (conex.rs.next());

	} catch (Exception ex) {
	    JOptionPane.showMessageDialog(null, "Erro ao preencher ArrayList" + ex);
	}
	ModeloTabela modelo = new ModeloTabela(dados, colunas);
	tbl_usuario.setModel(modelo);
	tbl_usuario.getColumnModel().getColumn(0).setPreferredWidth(40);
	tbl_usuario.getColumnModel().getColumn(0).setResizable(false);

	tbl_usuario.getColumnModel().getColumn(1).setPreferredWidth(200);
	tbl_usuario.getColumnModel().getColumn(1).setResizable(false);

	tbl_usuario.getColumnModel().getColumn(2).setPreferredWidth(120);
	tbl_usuario.getColumnModel().getColumn(2).setResizable(false);

	tbl_usuario.getColumnModel().getColumn(3).setPreferredWidth(150);
	tbl_usuario.getColumnModel().getColumn(3).setResizable(false);

	tbl_usuario.getColumnModel().getColumn(4).setPreferredWidth(100);
	tbl_usuario.getColumnModel().getColumn(4).setResizable(false);

	tbl_usuario.getTableHeader().setReorderingAllowed(false);
	tbl_usuario.setAutoResizeMode(tbl_usuario.AUTO_RESIZE_OFF);
	tbl_usuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
	conex.desconecta();
    }

