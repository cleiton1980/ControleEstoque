    private void bt_pesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_pesquisarActionPerformed
	String pesquisa = txt_pesquisar.getText();
	if (pesquisa.equals("")) {
	    JOptionPane.showMessageDialog(null, "Digite um Usuario a ser pesquisado");
	} else {
	    mod.setPesquisa(txt_pesquisar.getText());
	    ModeloUsuarios model = control.pesquisarUsuarios(mod);
	    lbl_idUsuario.setText(String.valueOf(model.getId_usuario()));
	    txt_nome.setText(model.getNome_usuario());
	    txt_login.setText(model.getLogin());
	    txt_senha.setText(model.getSenha_usuario());
	    cb_permissao.setSelectedItem(model.getPerfil_usuario());
	    bt_editar.setEnabled(true);
	    bt_excluir.setEnabled(true);
	}
    }//GEN-LAST:event_bt_pesquisarActionPerformed

    private void tbl_usuarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_usuarioMouseClicked
	String nome_usuario = "" + tbl_usuario.getValueAt(tbl_usuario.getSelectedRow(), 1);
	conex.Conexao();
	conex.executaSql("select * from tbusuarios where nome_usuario='" + nome_usuario + "'");
	try {
	    conex.rs.first();
	    lbl_idUsuario.setText(Integer.toString(conex.rs.getInt("id_usuario")));
	    txt_nome.setText(conex.rs.getString("nome_usuario"));
	    txt_login.setText(conex.rs.getString("login"));
	    cb_permissao.setSelectedItem(conex.rs.getString("perfil"));
	    txt_senha.setText(conex.rs.getString("senha"));
	} catch (SQLException ex) {
	    JOptionPane.showMessageDialog(null, "Erro ao selecionar os dados" + ex);
	}
	conex.desconecta();
	bt_salvar.setEnabled(false);
	bt_editar.setEnabled(true);
	bt_excluir.setEnabled(true);
	bt_cancelar.setEnabled(true);
	bt_novo.setEnabled(false);
	txt_nome.setEnabled(true);
	txt_login.setEnabled(true);
	txt_senha.setEnabled(true);
	cb_permissao.setEnabled(true);
    }//GEN-LAST:event_tbl_usuarioMouseClicked

    private void bt_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_salvarActionPerformed
	if (txt_nome.getText().isEmpty() || txt_senha.getText().isEmpty()) {
	    JOptionPane.showMessageDialog(null, "Há campos a serem preenchidos");
	} else {
	    mod.setNome_usuario(txt_nome.getText());
	    mod.setLogin(txt_login.getText());
	    mod.setSenha_usuario(txt_senha.getText());
	    mod.setPerfil_usuario(cb_permissao.getSelectedItem().toString());
	    control.Adicionar(mod);
	    preencherTabela("select * from tbusuarios order by nome_usuario");
	}
	txt_nome.setText("");
	txt_login.setText("");
	txt_senha.setText("");
    }//GEN-LAST:event_bt_salvarActionPerformed

